package thaigo.object;

/**
 * Model of <code>Pawn</code>.
 * 
 * @author Nol Pasurapunya 5510546018
 * @version 9/5/2013
 *
 */
public enum PawnModel {

	Classic("classic1.png","classic2.png"),
	Jurassic("jurassic1.png","jurassic2.png"),
	Doodle("doodle1.png","doodle2.png");
	
	private String firstPawn;
	private String secondPawn;
	
	private PawnModel(String first, String second){
		firstPawn = first;
		secondPawn = second;
	}
	
	/**
	 * Return first type of <code>Pawn</code>
	 * @return first type of <code>Pawn</code>
	 */
	public String getFirstPawn(){
		return "images/" + firstPawn;
	}
	
	/**
	 * Return second type of <code>Pawn</code>
	 * @return second type of <code>Pawn</code>
	 */
	public String getSecondPawn(){
		return "images/" + secondPawn;
	}
}
