package bagaturchess.bitboard.impl.utils;

import java.util.Comparator;

public class Comparator23 implements Comparator<long[]> {
	
	public int compare(long[] move1, long[] move2) {

		final long eval1 = move1[23];
		final long eval2 = move2[23];
		
		if (eval1 > eval2) {
			return -1;
		}
			
		if (eval1 < eval2) {
			return 1;
		}
		
		return 1;
	}
}
