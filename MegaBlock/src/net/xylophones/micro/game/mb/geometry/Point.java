package net.xylophones.micro.game.mb.geometry;

/**
 * A simple class consisting of an x,y set of coordinates
 */
public class Point {

	private int x;
	
	private int y;
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
}
