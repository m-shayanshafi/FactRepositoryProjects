package net.sf.jdivelog.model.uddf.file;

/**Represents the WayAltitude Uddf tag
 * 
 * @author Levtraru
 *
 */
public class UddfFileWayAltitude{

    private String waytime;
    private String altitude;
	public String getWaytime() {
		return waytime;
	}
	public void setWaytime(String waytime) {
		this.waytime = waytime;
	}
	public String getAltitude() {
		return altitude;
	}
	public void setAltitude(String altitude) {
		this.altitude = altitude;
	}
}