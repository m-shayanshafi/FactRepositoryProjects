package net.sf.jdivelog.model.uddf.file;

/**Represents the Mix Uddf tag
 * 
 * @author Levtraru
 *
 */
public class UddfFileMix {
	private String id;
	private String ar;
	private String equivalentairdepth;
	private String h2;
	private String he;
	private String n2;
	private String name;
	private String maximumoperationdepth;
	private String maximumpo2;
	private String o2;
	private String priceperlitre;
	private String priceperlitreCurrency;

	
	public String getPriceperlitreCurrency() {
		return priceperlitreCurrency;
	}

	public void setPriceperlitreCurrency(String priceperlitreCurrency) {
		this.priceperlitreCurrency = priceperlitreCurrency;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	public String getAr() {
		return ar;
	}

	public void setAr(String ar) {
		this.ar = ar;
	}

	public String getEquivalentairdepth() {
		return equivalentairdepth;
	}

	public void setEquivalentairdepth(String equivalentairdepth) {
		this.equivalentairdepth = equivalentairdepth;
	}

	public String getH2() {
		return h2;
	}

	public void setH2(String h2) {
		this.h2 = h2;
	}

	public String getHe() {
		return he;
	}

	public void setHe(String he) {
		this.he = he;
	}

	public String getN2() {
		return n2;
	}

	public void setN2(String n2) {
		this.n2 = n2;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMaximumoperationdepth() {
		return maximumoperationdepth;
	}

	public void setMaximumoperationdepth(String maximumoperationdepth) {
		this.maximumoperationdepth = maximumoperationdepth;
	}

	public String getMaximumpo2() {
		return maximumpo2;
	}

	public void setMaximumpo2(String maximumpo2) {
		this.maximumpo2 = maximumpo2;
	}

	public String getO2() {
		return o2;
	}

	public void setO2(String o2) {
		this.o2 = o2;
	}

	public String getPriceperlitre() {
		return priceperlitre;
	}

	public void setPriceperlitre(String priceperlitre) {
		this.priceperlitre = priceperlitre;
	}

}
