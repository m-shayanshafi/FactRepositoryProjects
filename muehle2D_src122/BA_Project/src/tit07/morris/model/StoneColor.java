package tit07.morris.model;

/**
 * Die Enumeration StoneColor repräsentiert die möglichen Farben eines
 * Spielsteins.
 */
public enum StoneColor {

    /**
     * Weißer Spielstein
     */
    WHITE,

    /**
     * Schwarzer Spielstein
     */
    BLACK,

    /**
     * Kein Spielstein (im Sinne des Spiels)
     */
    NONE
}
